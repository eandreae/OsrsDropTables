package WeightTables;

public class UniqueItem
{
    public String name;
    public int weight;

    public UniqueItem(String name, int weight)
    {
        this.name = name;
        this.weight = weight;
    }

    public String getName()
    {
        return this.name;
    }

    public int getWeight()
    {
        return this.weight;
    }
        
}